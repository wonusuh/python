class BigNum:
    
    digit = [
        "000000...00...00...000000",
        "....1....1....1....1....1",
        "22222....2222222....22222",
        "33333....333333....333333",
        "4...44...444444....4....4",
        "555555....55555....555555",
        "666666....666666...666666",
        "77777....7....7....7....7",
        "888888...8888888...888888",
        "999999...999999....999999",
        ".......+..+++++..+.......",# 10 +
        "..........-----..........",# 11 -
        "......x.x...x...x.x......",# 12 x
        "......../.../.../........",# 13 /
        ".....=====.....=====.....",# 14 =
        ]
    myBigNum = ""

    def __init__(self, num):
        self.num = num

    def big(self):
        for x in range(5):
            n = []
            for y in range(5):
                n.append(self.digit[self.num][x * 5 + y])
            self.myBigNum += "%s%s%s%s%s\n" %(n[0], n[1], n[2], n[3], n[4])
        print(self.myBigNum)
        return self.myBigNum

